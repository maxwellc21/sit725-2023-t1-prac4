document.addEventListener('DOMContentLoaded', function(){

    const cardData = [
        {
          title: "AI Gen Photo A",
          details: "The following Image was generated by the AI aswell ",
          image: "images/user-image.png"
        },
        {
            title: "AI Gen Photo A",
            details: "The following Image was generated by the AI aswell ",
            image: "images/img1.jpg"
          },
        {
          title: "AI Gen Photo B",
          details: "The following Image was generated by the AI ",
          image: "images/img2.jpg"
        }
      ];
    
    const cards = document.querySelectorAll('.card');
    cards.forEach((card, index) => {
      const data = cardData[index];
      const title = card.querySelector('.card-title');
      const details = card.querySelector('.card-content p');
      const image = card.querySelector('.card-content img');
      const revealTitle = card.querySelector('.card-reveal .card-title');
      const revealDetails = card.querySelector('.card-reveal p');

      title.textContent = data.title;
      details.textContent = data.details;
      image.src = data.image;
      revealTitle.textContent = data.title;
      revealDetails.textContent = data.details;
    });
    cards.forEach((card, index) => {
        card.addEventListener('click', function () {
          card.classList.toggle('enlarged');
        });
      });
   
});